const jsonMapping = {
  derecho,
  educacion,
  informatica,
};

const changeReticula = function () {
  const selectReticula = document.getElementById("select-reticula").value;
  const selectedJson = jsonMapping[selectReticula];
  const contenedor = document.getElementById("contenedor");
  contenedor.innerHTML = "";

  Object.keys(selectedJson).forEach((semestre) => {
    // Creamos un contenedor para el semestre
    const semestreDiv = document.createElement("div");
    //Materias
    const divMateria = document.createElement("div");
    divMateria.className = "materias";
    // periodos
    const divPeriodos = document.createElement("div");
    divPeriodos.className = "periodo";

    semestreDiv.className = "container-print-reticula";

    divPeriodos.innerHTML = `<h4>${
      semestre.charAt(0).toUpperCase() + semestre.slice(1)
    } Cuatrimestre</h4>`;
    contenedor
      .appendChild(semestreDiv)
      .appendChild(divMateria)
      .appendChild(divPeriodos);

    // Recorremos los cursos de cada semestre
    selectedJson[semestre].forEach((curso) => {
      // Creamos un contenedor para el curso
      const {
        name,
        clave,
        obligatorio,
        campo1,
        campo2,
        campo3,
        campo4,
        documento,
      } = curso;
      let cursoDiv = document.createElement("div");
      cursoDiv.className = `${
        curso.name.startsWith("D") || curso.name.startsWith("I")
          ? "content-reticula"
          : "content-reticula2"
      }`;
      cursoDiv.innerHTML = `
        <div class="claves">
          <div class="clave"><p>${clave}</p></div>
          <div class="clave"><p>${campo1}</p></div>
          <div class="clave"><p>${
            obligatorio == 1 ? "Obligatorio" : "No obligatorio"
          }</p></div>
        </div>
        <div class="nombre-materia">
          <h5>${name}</h5>
        </div>
        <div class="claves">
          <div class="clave"><p>${campo2}</p></div>
          <div class="clave"><p>${campo3}</p></div>
          <div class="clave"><p>${campo4}</p></div>
          <div class="clave" onclick=abrirPdf("${documento}")><p data-tooltip="Ver pdf"><i class='bx bx-show'></i></p></div>
        </div>
      `;
      semestreDiv.appendChild(cursoDiv);
    });
  });
};

const getDriveFileId = function (url) {
  const regex = /\/d\/([a-zA-Z0-9_-]+)\//;
  const match = url.match(regex);
  return match ? match[1] : null;
};

function getPreviewLink(pdfUrl) {
  const fileId = getDriveFileId(pdfUrl);
  if (fileId) {
    return `https://drive.google.com/file/d/${fileId}/preview`;
  } else {
    return null;
  }
}

const conIframe = document.querySelector(".container-iframe");
conIframe.classList.add("hidden");
function abrirPdf(url) {
  const directPdfLink = getPreviewLink(url); // Pasar url a getPreviewLink
  if (directPdfLink) {
    const pdfIframe = document.getElementById("pdf-iframe");
    conIframe.classList.remove("hidden");
    conIframe.classList.add("show");
    pdfIframe.src = directPdfLink;
  } else {
    conIframe.classList.remove("hidden");
    conIframe.classList.add("show");
    const avisoPdf = document.getElementById("aviso");
    avisoPdf.textContent =
      "No se pudo obtener el enlace de previsualización del PDF";
    const pdfIframe = document.getElementById("pdf-iframe");
    pdfIframe.src = "";
  }
}

function cerrarModal() {
  const avisoPdf = document.getElementById("aviso");
  avisoPdf.textContent = "";
  conIframe.classList.add("hidden");
  conIframe.classList.remove("show");
}
